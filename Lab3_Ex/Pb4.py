def hex_to_bin(hex_num):
    return f"{hex_num:08b}"

def hex_list_to_matrix(hex_list):
    return [hex_to_bin(num) for num in hex_list]

def format_matrices(matrices):
    formatted_lines = []
    for row in range(8):
        line = ""
        for matrix in matrices:
            line += matrix[row] + " "
        formatted_lines.append(line.strip())
    return "\n".join(formatted_lines)

def write_output(output_file, formatted_matrices):
    with open(output_file, 'w') as file:
        file.write(formatted_matrices)

